from directedGraph import *

print("\nTesting strongly connected components:")
g1 = directedGraph(5)
g1.add_edge(1, 0)
g1.add_edge(0, 2)
g1.add_edge(2, 1)
g1.add_edge(0, 3)
g1.add_edge(3, 4)
print("SSC in first graph:")
g1.SCC()

g2 = directedGraph(4)
g2.add_edge(0, 1)
g2.add_edge(1, 2)
g2.add_edge(2, 3)
print("SSC in the second graph:")
g2.SCC()

